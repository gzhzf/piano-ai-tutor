ALL_REPERTOIRE.update(INTERMEDIATE_REPERTOIRE)

# 难度分级索引
LEVEL_INDEX = {
    "零基础": [k for k,v in BEGINNER_REPERTOIRE.items() if "零基础" in v.get("难度","")],
    "预备级": [k for k,v in BEGINNER_REPERTOIRE.items() if "预备级" in v.get("难度","")],
    "1级": [k for k,v in {**BEGINNER_REPERTOIRE, **INTERMEDIATE_REPERTOIRE}.items() if "1级" in v.get("难度","") and "2级" not in v.get("难度","") and "3级" not in v.get("难度","")],
    "2级": [k for k,v in INTERMEDIATE_REPERTOIRE.items() if "2级" in v.get("难度","") and "3级" not in v.get("难度","")],
    "3级": [k for k,v in INTERMEDIATE_REPERTOIRE.items() if "3级" in v.get("难度","")],
}

def get_repertoire_by_level(level):
    """按难度获取曲目列表"""
    return LEVEL_INDEX.get(level, [])

def search_repertoire(query):
    """搜索曲目"""
    results = []
    for name, info in ALL_REPERTOIRE.items():
        if query in name or query in str(info):
            results.append({"name": name, "info": info})
    return results

def get_all_repertoire_names():
    """获取所有曲目名称"""
    return list(ALL_REPERTOIRE.keys())

