def search_knowledge(query):
    """简单知识检索（模拟RAG）"""
    results = []
    query_lower = query.lower()
    for level_name, kb in get_all_knowledge().items():
        for topic, content in kb.items():
            if any(k in query for k in [topic, topic[:2]]) or \
               any(k in query for k in str(content).split('\"')[1:3] if len(k) > 1):
                results.append({"level": level_name, "topic": topic, "content": content})
    return results[:3]
